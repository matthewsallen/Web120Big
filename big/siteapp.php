 <h2 class="pageID">Responsive Sites vs. Web Apps</h2>
 <p>
     Responsive websites are accessed through a web browser just as an unresponsive site. However, they are more versatile and can change size and layout depending on the screen size, browser or device one is using. Mobile applications are software downloaded to a device that can perform similar functions as a website but are better suited to a mobile device. Websites are good for accessing information, but apps are better for completing a task.<br><br>

     It is hard to compare responsive websites to mobile apps because their appropriateness depends on context. I believe it is better to stay with a website for uses like small business or events since the user is probably not going to use the site frequently and requiring the download of an app would be seen as unnecessary. For example, the website I am creating for this class does not need a mobile app because it is informational, used infrequently, and there are not tasks to be completed. Responsive sites are easier and cheaper to build and maintain and are accessible to all users, rather than just those with mobile devices. Websites also can be hosted independently, and don’t have to be filtered through Google or Apple’s app stores (Thinkmobiles, 2019)<br><br>

     An example of a site where an app would be better suited would be a ‘reservation’ site. Many restaurant websites link externally to apps like OpenTable or Resy. There, a user’s account can keep contact information and history stored for easy and efficient use at a later time. Apps are often more intuitive with clear buttons and fields, and less clutter. They can also include push notifications and link to other apps like PayPal or Google Maps. Another benefit of an app is that it can use the device’s components such as GPS, HD cameras and microphones.<br><br>

     Usage of mobile apps vs. websites on mobile devices has skyrocketed, and over 80% of time spent on a mobile device is on an app (Edwards, 2014). I expect that this trend will continue as in most cases an app is more functional and efficient on a mobile device. I also think that websites have a purpose, such as researching for this essay. 


